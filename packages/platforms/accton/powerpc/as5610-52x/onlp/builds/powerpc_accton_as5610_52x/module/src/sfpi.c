int
onlp_sfpi_enable_set(int port, int enable)
{
    /* If enable is 1, enable the SFP */
    /* If enable is 0, disable the SFP */
    int i = 0;
    unsigned char buf;
    sfp_io_expander_hierarchy_t *p = sfp_enable_data;

    for (i = 0; i < AIM_ARRAYSIZE(sfp_enable_data); i++) {
        if (port != p[i].port) {
            continue;
        }

        /* Set multiplexer to the corresponded channel */
        if (i2c_nWrite(p[i].bus, p[i].mux.addr, 0, sizeof(p[i].mux.ch), &p[i].mux.ch) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        /* Set the configuration register of IO-expander as output */
        if (i2c_nRead(p[i].bus, p[i].io_exp.addr, p[i].io_exp.cfg_reg, sizeof(buf), &buf) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        buf &= ~p[i].io_exp.status_mask;

        if (i2c_nWrite(p[i].bus, p[i].io_exp.addr, p[i].io_exp.cfg_reg, sizeof(buf), &buf) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        /* Read from the output register to get the current enable state */
        if (i2c_nRead(p[i].bus, p[i].io_exp.addr, p[i].io_exp.status_reg + 0x8, sizeof(buf), &buf) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        /* Do the enable/disable action */
        buf = enable ? (buf | p[i].io_exp.status_mask) : (buf & ~p[i].io_exp.status_mask);

        if (i2c_nWrite(p[i].bus, p[i].io_exp.addr, p[i].io_exp.status_reg + 0x8, sizeof(buf), &buf) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        return ONLP_STATUS_OK;
    }

    return ONLP_STATUS_E_MISSING;
}

