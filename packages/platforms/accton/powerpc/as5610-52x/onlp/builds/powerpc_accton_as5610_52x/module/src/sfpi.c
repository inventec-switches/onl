int
onlp_sfpi_is_present(int port)
{
    /*
     * Return 1 if present.
     * Return 0 if not present.
     * Return < 0 if error.
     */
    int i = 0;
    unsigned char buf;

    for (i = 0; i < AIM_ARRAYSIZE(sfp_psnt_data); i++)
    {
        if (port != sfp_psnt_data[i].port) {
            continue;
        }

        /* Set multiplexer to the corresponded channel */
        if (i2c_nWrite(sfp_psnt_data[i].bus, sfp_psnt_data[i].mux.addr, 0, sizeof(sfp_psnt_data[i].mux.ch), &sfp_psnt_data[i].mux.ch) != 0) {
            return 0;
        }

        /* Set the configuration register of IO-expander as input */
        if (i2c_nWrite(sfp_psnt_data[i].bus, sfp_psnt_data[i].io_exp.addr,
                      sfp_psnt_data[i].io_exp.cfg_reg, sizeof(sfp_psnt_data[i].io_exp.status_mask), &sfp_psnt_data[i].io_exp.status_mask) != 0) {
            return 0;
        }

        /* Read from the input register to get the present state */
        if (i2c_nRead(sfp_psnt_data[i].bus, sfp_psnt_data[i].io_exp.addr,
                     sfp_psnt_data[i].io_exp.status_reg, sizeof(buf), &buf) != 0) {
            return 0;
        }

        return !(buf & sfp_psnt_data[i].io_exp.status_mask);
    }

    return 0;
}

static void
port_qsfp_ioexpander_presence_map__(int port, int* rport)
{
    switch(port)
    {
        case 48: *rport = 49; break;
        case 49: *rport = 48; break;
        case 50: *rport = 51; break;
        case 51: *rport = 50; break;
        default: *rport = port; break;
    }
}

int
onlp_sfpi_presence_bitmap_get(onlp_sfp_bitmap_t* dst)
{
    int i = 0;
    unsigned char bytes[7];
    mux_info_t mux = {0x76, 0x8};

    /* Set multiplexer to the corresponded channel */
    if (i2c_nWrite(1, mux.addr, 0, sizeof(mux.ch), &mux.ch) != 0) {
        return ONLP_STATUS_E_INTERNAL;
    }

    for (i = 0; i < AIM_ARRAYSIZE(sfp_psnt_all_data); i++)
    {
        /* Set the configuration register of IO-expander as input */
        if (i2c_nWrite(1, sfp_psnt_all_data[i].addr, sfp_psnt_all_data[i].cfg_reg,
                       sizeof(sfp_psnt_all_data[i].status_mask), &sfp_psnt_all_data[i].status_mask) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        /* Read from the input register to get the present state */
        if (i2c_nRead(1, sfp_psnt_all_data[i].addr, sfp_psnt_all_data[i].status_reg,
                      sizeof(bytes[i]), &bytes[i]) != 0) {
            return ONLP_STATUS_E_INTERNAL;
        }

        bytes[i] = ~bytes[i];
    }

    /* Mask out non-existant QSFP ports */
    bytes[6] &= 0xF;

    /* Convert to 64 bit integer in port order */
    uint64_t presence_all = 0 ;

    for(i = AIM_ARRAYSIZE(bytes)-1; i >= 0; i--) {
        presence_all <<= 8;
        presence_all |= bytes[i];
    }

    /* Populate bitmap */
    for(i = 0; presence_all; i++) {
        int p;
        port_qsfp_ioexpander_presence_map__(i, &p);
        AIM_BITMAP_MOD(dst, p, (presence_all & 1));
        presence_all >>= 1;
    }

    return ONLP_STATUS_OK;
}

