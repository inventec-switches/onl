    /* init equalizer */
    if (ONLP_STATUS_OK != init_equalizer()) {
        AIM_LOG_ERROR("init_equalizer failed\r\n");
        return ONLP_STATUS_E_INTERNAL;
    }

    /* init qsfp modsel as low */
