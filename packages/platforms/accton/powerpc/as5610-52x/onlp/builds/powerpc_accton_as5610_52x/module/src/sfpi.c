{
    int ret = ONLP_STATUS_OK;
    int sfp_idx = 0, i = 0;

    for (sfp_idx = 0; sfp_idx < AIM_ARRAYSIZE(sfp_eeprom_data); sfp_idx++) {
        if (sfp_eeprom_data[sfp_idx].port == port) {
            break;
        }
    }

    if (sfp_idx == AIM_ARRAYSIZE(sfp_eeprom_data)) {
        return ONLP_STATUS_E_MISSING;
    }

    // Step 1: Change the mux to the correct channel for the specified port number
    for (i = 0; i < MAX_MUX_COUNT; i++) {
        if (sfp_eeprom_data[sfp_idx].mux[i].addr == 0) {
            break;
        }

        if (0 != i2c_nWrite(sfp_eeprom_data[sfp_idx].bus,
                           sfp_eeprom_data[sfp_idx].mux[i].addr, 0x0,
                            sizeof(sfp_eeprom_data[sfp_idx].mux[i].ch),
                            &sfp_eeprom_data[sfp_idx].mux[i].ch)) {
            AIM_LOG_ERROR("Line = (%d): I2C write failed, bus=%d, addr=%d, ch = %d\r\n",
                           __LINE__, sfp_eeprom_data[sfp_idx].bus,
                          sfp_eeprom_data[sfp_idx].mux[i].addr,  sfp_eeprom_data[sfp_idx].mux[i].ch);
            return ONLP_STATUS_E_INTERNAL;
        }
    }

    // Step 2: Set eeprom byte 93(0x5D) bit0 (power-over-ride)
    if (i2c_nWrite(sfp_eeprom_data[sfp_idx].bus, SFP_IDPROM_ADDR, 0x5D, sizeof(enable), &enable) != 0) {
        AIM_LOG_ERROR("Line = (%d): I2C write LPMode failed, bus=%d, addr=%d, ch = %d\r\n",
                      __LINE__, sfp_eeprom_data[sfp_idx].bus, sfp_eeprom_data[sfp_idx].mux[i].addr);
        ret = ONLP_STATUS_E_INTERNAL;
    }

    // Step 3: Turn off the channel
    for (i = (MAX_MUX_COUNT-1); i >= 0; i--) {
        unsigned char buf = 0;

        if (sfp_eeprom_data[sfp_idx].mux[i].addr == 0) {
            continue;
        }

        if (0 != i2c_nWrite(sfp_eeprom_data[sfp_idx].bus,
                            sfp_eeprom_data[sfp_idx].mux[i].addr, 0x0, sizeof(buf), &buf)) {
            AIM_LOG_ERROR("Line = (%d): I2C write failed, bus=%d, addr=%x, ch = %d, sfp_idx=(%d)\r\n",
                           __LINE__, sfp_eeprom_data[sfp_idx].bus,
                          sfp_eeprom_data[sfp_idx].mux[i].addr, buf, sfp_idx);
            ret = ONLP_STATUS_E_INTERNAL;
        }
    }

    /* Sleep 1ms for waiting power over ride mode to take place */
    usleep(1000);

    return ret;
}

int
onlp_sfpi_control_set(int port, onlp_sfp_control_t control, int value)
{
    switch(control)
        {
        case ONLP_SFP_CONTROL_POWER_OVERRIDE:
            return control_set_power_override__(port, value);
        default:
            return ONLP_STATUS_E_UNSUPPORTED;
        }
}

int
onlp_sfpi_control_get(int port, onlp_sfp_control_t control, int* value)
{
    switch(control)
        {

        case ONLP_SFP_CONTROL_RX_LOS:
            *value = is_rxloss(port) ? 1 : 0;
            return ONLP_STATUS_OK;

        case ONLP_SFP_CONTROL_TX_FAULT:
            *value = is_txfailed(port) ? 1 : 0;
            return ONLP_STATUS_OK;

        default:
            return ONLP_STATUS_E_UNSUPPORTED;

        }

}

